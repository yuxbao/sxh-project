mysql_exec() {
  mysql -h"${MYSQL_HOST}" -P"${MYSQL_PORT}" -u"${MYSQL_USER}" -p"${MYSQL_PASSWORD}" -D "${SXH_DB_NAME}" -N -B -e "$1"
}

sql_escape() {
  local value="$1"
  value="${value//\\/\\\\}"
  value="${value//\'/\'\'}"
  printf '%s' "$value"
}

json_escape() {
  local value="$1"
  value="${value//\\/\\\\}"
  value="${value//\"/\\\"}"
  value="${value//$'\n'/\\n}"
  value="${value//$'\r'/}"
  printf '%s' "$value"
}

ensure_category() {
  local category_name="$1"
  local escaped_name
  escaped_name="$(sql_escape "${category_name}")"
  mysql_exec "INSERT INTO category (category_name, status, \`rank\`, deleted)
              SELECT '${escaped_name}', 1, 0, 0
              FROM DUAL
              WHERE NOT EXISTS (
                SELECT 1 FROM category WHERE category_name = '${escaped_name}' AND deleted = 0
              );"
  mysql_exec "SELECT id FROM category WHERE category_name = '${escaped_name}' AND deleted = 0 ORDER BY id DESC LIMIT 1"
}

ensure_tag() {
  local tag_name="$1"
  local category_id="$2"
  local escaped_name
  escaped_name="$(sql_escape "${tag_name}")"
  mysql_exec "INSERT INTO tag (tag_name, tag_type, category_id, status, deleted)
              SELECT '${escaped_name}', 1, ${category_id}, 1, 0
              FROM DUAL
              WHERE NOT EXISTS (
                SELECT 1 FROM tag WHERE tag_name = '${escaped_name}' AND deleted = 0
              );"
  mysql_exec "SELECT id FROM tag WHERE tag_name = '${escaped_name}' AND deleted = 0 ORDER BY id DESC LIMIT 1"
}

ensure_read_count() {
  local article_id="$1"
  mysql_exec "INSERT INTO read_count (document_id, document_type, cnt)
              SELECT ${article_id}, 1, 0
              FROM DUAL
              WHERE NOT EXISTS (
                SELECT 1 FROM read_count WHERE document_id = ${article_id} AND document_type = 1
              );"
}

sync_article() {
  local article_id="$1"
  local title="$2"
  local summary="$3"
  local content="$4"
  local category_name="$5"
  local tags_csv="$6"
  local article_url="${SXH_FRONTEND_URL%/}/article/detail/${article_id}"
  local title_json summary_json content_json category_json author_json article_url_json
  local tags_json=""

  title_json="$(json_escape "${title}")"
  summary_json="$(json_escape "${summary}")"
  content_json="$(json_escape "${content}")"
  category_json="$(json_escape "${category_name}")"
  author_json="$(json_escape "${AUTHOR_NAME}")"
  article_url_json="$(json_escape "${article_url}")"

  IFS=',' read -r -a tags <<< "${tags_csv}"
  for tag in "${tags[@]}"; do
    tag="$(echo "${tag}" | xargs)"
    if [[ -n "${tag}" ]]; then
      if [[ -n "${tags_json}" ]]; then
        tags_json+=","
      fi
      tags_json+="\"$(json_escape "${tag}")\""
    fi
  done

  curl -sSf "${RAG_BASE_URL%/}/api/v1/integration/articles/upsert" \
    -H "Content-Type: application/json" \
    -H "X-Internal-Token: ${RAG_INTERNAL_TOKEN}" \
    --data-binary @- >/dev/null <<JSON
{
  "articleId": ${article_id},
  "authorId": ${AUTHOR_ID},
  "authorName": "${author_json}",
  "title": "${title_json}",
  "summary": "${summary_json}",
  "content": "${content_json}",
  "categoryName": "${category_json}",
  "tags": [${tags_json}],
  "articleUrl": "${article_url_json}"
}
JSON
}

seed_article() {
  local title="$1"
  local short_title="$2"
  local summary="$3"
  local category_id="$4"
  local category_name="$5"
  local tags_csv="$6"
  local content="$7"
  local escaped_title escaped_short_title escaped_summary escaped_content
  local article_id

  escaped_title="$(sql_escape "${title}")"
  escaped_short_title="$(sql_escape "${short_title}")"
  escaped_summary="$(sql_escape "${summary}")"
  escaped_content="$(sql_escape "${content}")"

  mysql_exec "INSERT INTO article (
                user_id, article_type, title, short_title, picture, summary,
                category_id, source, source_url, offical_stat, topping_stat,
                cream_stat, status, deleted
              )
              SELECT ${AUTHOR_ID}, 1, '${escaped_title}', '${escaped_short_title}', '', '${escaped_summary}',
                     ${category_id}, 2, '', 1, 0, 0, 1, 0
              FROM DUAL
              WHERE NOT EXISTS (
                SELECT 1 FROM article WHERE title = '${escaped_title}' AND deleted = 0
              );"

  article_id="$(mysql_exec "SELECT id FROM article WHERE title = '${escaped_title}' AND deleted = 0 ORDER BY id DESC LIMIT 1")"

  mysql_exec "INSERT INTO article_detail (article_id, version, content, deleted)
              SELECT ${article_id}, 1, '${escaped_content}', 0
              FROM DUAL
              WHERE NOT EXISTS (
                SELECT 1 FROM article_detail WHERE article_id = ${article_id} AND version = 1 AND deleted = 0
              );"

  IFS=',' read -r -a tags <<< "${tags_csv}"
  for tag_name in "${tags[@]}"; do
    local tag_id
    tag_name="$(echo "${tag_name}" | xargs)"
    tag_id="$(mysql_exec "SELECT id FROM tag WHERE tag_name = '$(sql_escape "${tag_name}")' AND deleted = 0 ORDER BY id DESC LIMIT 1")"
    mysql_exec "INSERT INTO article_tag (article_id, tag_id, deleted)
                SELECT ${article_id}, ${tag_id}, 0
                FROM DUAL
                WHERE NOT EXISTS (
                  SELECT 1 FROM article_tag WHERE article_id = ${article_id} AND tag_id = ${tag_id} AND deleted = 0
                );"
  done

  ensure_read_count "${article_id}"
  sync_article "${article_id}" "${title}" "${summary}" "${content}" "${category_name}" "${tags_csv}"
  printf 'Seeded article: %s -> %s\n' "${article_id}" "${title}"
}

