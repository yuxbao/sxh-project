## 缓存穿透

查询一个原本就不存在的数据，请求每次都会落到数据库。常见做法是缓存空值，或者在入口做布隆过滤。

## 缓存击穿

某个热点 key 恰好失效，大量请求瞬间同时去查数据库。应对方式通常是热点永不过期、互斥锁重建缓存，或者提前续期。

## 缓存雪崩

大量 key 在同一时间失效，或者缓存服务整体不可用，导致后端瞬时压力暴增。处理重点是过期时间打散、限流降级、多级缓存。

